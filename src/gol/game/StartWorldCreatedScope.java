package gol.game;

public class StartWorldCreatedScope {

	private final int width;
	private final int height;

	public StartWorldCreatedScope(final int width, final int height) {
		this.width = width;
		this.height = height;
	}

	public int getWidth() {
		return width;
	}

	public int getHeight() {
		return height;
	}
}
